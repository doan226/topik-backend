        JavaMailSenderImpl mailSender = new JavaMailSenderImpl();
        mailSender.setHost("smtp.gmail.com");
        mailSender.setPort(587);
        mailSender.setUsername("support.topikai@gmail.com"); // Thay email của bạn
        mailSender.setPassword("cqll ipkt zwhd ymzj");    // Thay mật khẩu ứng dụng 16 ký tự

